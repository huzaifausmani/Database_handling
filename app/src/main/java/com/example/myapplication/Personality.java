package com.example.myapplication;

public class Personality {
    public String name;
    public int introvert;
    public int extrovert;
    public int ambivert;
    public int psychopath;
    public int sociopath;

    public Personality(String name, int introvert, int extrovert, int ambivert, int psychopath, int sociopath){
        this.name = name;
        this.introvert = introvert;
        this.extrovert = extrovert;
        this.ambivert = ambivert;
        this.psychopath = psychopath;
        this.sociopath = sociopath;
    }
    

}
